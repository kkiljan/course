package pl.kkiljan.lesson02;

public class cheetah {

    private double speed;
    private double weight;

    public cheetah() {
    }

    public cheetah(double speed, double weight) {
        this.speed = speed;
        this.weight = weight;

    }

    double getSpeed() {
        return speed;

    }

    double getWeight() {
        return weight;
    }

}
